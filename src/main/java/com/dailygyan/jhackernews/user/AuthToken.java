package com.dailygyan.jhackernews.user;

public class AuthToken {

    String token;

    /**
     * @return the token
     */
    public String getToken() {
        return token;
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        return "AuthToken [token=" + token + "]";
    }

}
